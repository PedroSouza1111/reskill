CREATE TABLE TB_RESKILL_AREA_INTERESSE (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR(255)
);

CREATE TABLE TB_RESKILL_CURSO (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR(255),
    link VARCHAR(255),
    plataforma VARCHAR(255)
);

CREATE TABLE TB_RESKILL_USUARIO (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR(255),
    email VARCHAR(255) UNIQUE
);

CREATE TABLE TB_USUARIO_AREA_INTERESSE (
    usuario_id BIGINT NOT NULL,
    area_interesse_id BIGINT NOT NULL,
    PRIMARY KEY (usuario_id, area_interesse_id),
    FOREIGN KEY (usuario_id) REFERENCES TB_RESKILL_USUARIO(id),
    FOREIGN KEY (area_interesse_id) REFERENCES TB_RESKILL_AREA_INTERESSE(id)
);

CREATE TABLE TB_USUARIO_CURSO_CONCLUIDO (
    usuario_id BIGINT NOT NULL,
    curso_id BIGINT NOT NULL,
    PRIMARY KEY (usuario_id, curso_id),
    FOREIGN KEY (usuario_id) REFERENCES TB_RESKILL_USUARIO(id),
    FOREIGN KEY (curso_id) REFERENCES TB_RESKILL_CURSO(id)
);